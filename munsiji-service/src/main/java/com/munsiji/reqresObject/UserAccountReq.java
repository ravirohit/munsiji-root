package com.munsiji.reqresObject;

public class UserAccountReq {
	String name;
	String type;
	Float investedAmnt;
	String desc;
	String crteDate;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public Float getInvestedAmnt() {
		return investedAmnt;
	}
	public void setInvestedAmnt(Float investedAmnt) {
		this.investedAmnt = investedAmnt;
	}
	public String getCrteDate() {
		return crteDate;
	}
	public void setCrteDate(String crteDate) {
		this.crteDate = crteDate;
	}
	
    
	
}
